package com.dti;

public class Test01 {
    //Ctrl+Alt+L จัดระเบียบโค็ด , Ctrl + D คัดลอกบรรทัด
    //เรียกเมธอด
    public static void main(String[] args) {
        System.out.println("AAAAA");    //String
        System.out.print("BBBBB");
        System.out.print("CCCCC");
        System.out.println("DDDDD");
        System.out.print("EEEEE");
        System.out.print(6546);         //number (byte, short, int, long)
        System.out.print(65.5446);      //number (float,double)
        System.out.print(true);         //boolean
        System.out.print(false);
        System.out.println(10 + 20);      //expression นิพจน์
        System.out.println('A');          //character
    }
}
